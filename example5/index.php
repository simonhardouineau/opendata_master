    $("document").ready(function() {


        var scale = chroma.scale(['#ff5354', '#ffc147', '#70f990']).mode('lab').domain([.50, .70, .90]),
            $marker = $("#marker").clone(),
            getIcon = function(value) {
                var $tmp_marker = $marker.clone(),
                $tmp_paths = $tmp_marker.find("#Contenu").find("ellipse, path:not(#SVGID_1_)");
                $tmp_paths.each(function(index, elem) {
                    if (index >= value) {
                        $(elem).attr("fill", "none");
                        if (index == value) {
                            $(elem).attr("fill", scale(value).brighten().hex());
                        }
                    } else {
                        $(elem).attr("fill", scale(value).hex());

                    }
                });

                return btoa(new XMLSerializer().serializeToString($tmp_marker[0]));

            };

