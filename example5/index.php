            var vectorSource = new ol.source.Vector({
                features: iconFeatures
            });

            var vectorLayer = new ol.layer.Vector({
                source: vectorSource,
                rendererOptions: {yOrdering: true},
            });

            var map = new ol.Map({
                target: 'map',
                layers: [
                    new ol.layer.Tile({
                        source: new ol.source.OSM()
                    }),
                    vectorLayer

